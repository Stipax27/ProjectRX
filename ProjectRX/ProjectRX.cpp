// Функция определения типа ответа для Соловья
int GetNpcDialogIndex(int playerPersuasion) {
    if (playerPersuasion == 3) return 0; // Мямля
    else if (playerPersuasion == 8) return 1; // Оптимальный
    else return 2; // Агрессивный
}

