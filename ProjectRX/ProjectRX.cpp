        string npcDialog;
        if (lastPlayerChoiceType == -1) {
            // Первый раунд - используем первый вариант
            npcDialog = rounds[currentRound].npcDialogs[0];
        }
        else {
            npcDialog = rounds[currentRound].npcDialogs[lastPlayerChoiceType];
        }

        DisplayNPCDialog(npcDialog);
        int nextRound = DisplayPlayerChoices(rounds[currentRound].choices, currentRound + 1);

        if (nextRound == -1) break; // Конец диалога
        currentRound = nextRound - 1; // Переходим к следующему раунду

        // Проверяем не спровоцировали ли бой
        if (provokedFight) {
            break;
        }

