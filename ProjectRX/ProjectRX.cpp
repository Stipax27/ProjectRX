// Функция для перемешивания вариантов ответов
vector<PlayerChoice> ShuffleChoices(const vector<PlayerChoice>& choices) {
    vector<PlayerChoice> shuffled = choices;
    random_device rd;
    mt19937 g(rd());
    shuffle(shuffled.begin(), shuffled.end(), g);
    return shuffled;
}

